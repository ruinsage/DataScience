
def Add(x,y):
    result = x + y
    return result

def Sub(x,y):
    result = x - y
    return result

def Mul(x,y):
    result = x * y
    return result

def Div(x,y):
    result = x / y
    return result

if __name__ == "__main__" :
    x = 10
    y = 2
    print(Add(x,y))
    print(Sub(x,y))
    print(Mul(x,y))
    print(Div(x,y))
